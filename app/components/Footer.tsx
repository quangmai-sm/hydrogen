function activeLinkStyle({
  isActive,
  isPending,
}: {
  isActive: boolean;
  isPending: boolean;
}) {
  return {
    fontWeight: isActive ? '500' : '400',
    color: isPending ? '#8b8b8b' : '#4b5563',
  };
}
